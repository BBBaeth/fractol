#include "fractol.h"

void	lets_scale(t_mlx *list)
{
	float	x1;
	float	x2;
	float	y1;
	float	y2;

	x1 = -2.1;
	x2 = 0.6;
	if (FCT == 2)
	{
		x1 = -1.85;
		x2 = 1.85;
	}
	y1 = -1.2;
	y2 = 1.2;
	ZOOM_X = IMG_L / (x2 - x1);
	ZOOM_Y = IMG_H / (y2 - y1);
}
